import numpy as np

print('ndarray 的花式索引，使用整数数组作为索引')
x = np.array([1, 2, 3, 4, 5, 6])
print(x[[0, 1, 2]])  # [1 2 3]
print(x[[-1, -2, -3]])  # [6 5 4]
x = np.array([[1, 2], [3, 4], [5, 6]])
print(x[[0, 1]])  # [1, 2], [3, 4]
print(x[[0, 1], [0, 1]])  # [1 4] 打印x[0][0]和x[1][1]
print(x[[0, 1]][:, [0, 1]])  # 打印01行的01列 [[1,2],[3,4]]
# 使用numpy.ix_()函数增强可读性
print(x[np.ix_([0, 1], [0, 1])])  # 同上 打印01行的01列 [[1,2],[3,4]]
